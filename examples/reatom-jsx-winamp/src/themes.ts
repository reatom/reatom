export const PLAYER_THEME_VARS: Record<PlayerThemeId, ThemeVars> = {
  classic,
  carbon,
  ember,
  aurora,
  neon,
}

export function isPlayerThemeId(value: string): value is PlayerThemeId {
  return value in PLAYER_THEME_VARS
}

export function serializeThemeVars(vars: ThemeVars): string {
  return Object.entries(vars)
    .map(([key, value]) => `        ${key}: ${value};`)
    .join('\n')
}
